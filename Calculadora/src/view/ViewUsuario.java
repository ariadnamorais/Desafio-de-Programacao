    //metodo de limpar formulário de usuário
    //esse metodo pode ser chamado em qualquer lugar da tela de usuário
    private void limparFormulario() {
        jtfCodigoUsu.setText("");
        jtfLoginUsua.setText("");
        jtfNomeUsua.setText("");
        jpfSenhaUsua.setText("");
        cadAlt = "cad";
    }

    /**
     * Carrega a lista de usuários cadastrados na tabela de usuários
     */
    private void carregarUsuarios() {
        listaUsuario = controllerUsuario.getListaUsuariosController();
        DefaultTableModel modelo = (DefaultTableModel) jtUsua.getModel();
        modelo.setNumRows(0); //limpa a tabela antes de exibir mais dados - serve para nao ficar acumulando dados

        for (int i = 0; i < listaUsuario.size(); i++) {
            modelo.addRow(new Object[]{
                listaUsuario.get(i).getUsuId(),
                listaUsuario.get(i).getUsuNome(),
                listaUsuario.get(i).getUsuLogin()
            });
        }
    }

