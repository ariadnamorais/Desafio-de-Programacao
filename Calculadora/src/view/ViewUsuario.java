    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        int linha = jtUsua.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione o usuário que deseja excluir");
        } else {
            int codigo = (int) jtUsua.getValueAt(linha, 0);
            if (controllerUsuario.excluirUsuarioController(codigo)) {
                carregarUsuarios();
                limparFormulario();
                JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso!", "Confirmação de Exclusão", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Não foi possível excluir o usuário");
            }
        }

    }//GEN-LAST:event_jbExcluirActionPerformed

    //Edita cadastro
    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // pegar o id para buscar no banco
        cadAlt = "alt";
        modelUsuario = new ModelUsuario();
        int linha = jtUsua.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione o usuário que deseja editar");
        } else {
            int codigo = (int) jtUsua.getValueAt(linha, 0);
            modelUsuario = controllerUsuario.getUsuarioController(codigo);
            jtfCodigoUsu.setText(String.valueOf(modelUsuario.getUsuId()));
            jtfLoginUsua.setText(modelUsuario.getUsuLogin());
            jtfNomeUsua.setText(modelUsuario.getUsuNome());
            jpfSenhaUsua.setText(modelUsuario.getUsuSenha());
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

