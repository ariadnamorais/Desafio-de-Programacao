    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // TODO add your handling code here:
        modelUsuario = new ModelUsuario();
        modelUsuario.setUsuNome(jtfNomeUsua.getText());
        modelUsuario.setUsuLogin(jtfLoginUsua.getText());
        modelUsuario.setUsuSenha(String.valueOf(jpfSenhaUsua.getPassword())); //String.valueOf Serve para converter string para o valor esperado
        if (cadAlt.equals("cad")) {
            if (controllerUsuario.salvarUsuarioController(modelUsuario)) {
                //cadastrou usuario
                JOptionPane.showMessageDialog(this, "Dados cadastrados com sucesso!", "Confirmação", JOptionPane.INFORMATION_MESSAGE);
                //limpar informações do Formulário
                limparFormulario();
                carregarUsuarios();
            } else {
                //não cadastrou  
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            modelUsuario.setUsuId(Integer.parseInt(jtfCodigoUsu.getText()));
            if (controllerUsuario.atualizarUsuarioController(modelUsuario)) {
                //alterou usuario
                JOptionPane.showMessageDialog(this, "Dados alterados com sucesso!", "Confirmação", JOptionPane.INFORMATION_MESSAGE);
                //limpar informações do Formulário
                limparFormulario();
                carregarUsuarios();
            } else {
                //não cadastrou  
                JOptionPane.showMessageDialog(this, "Erro ao editar cadastro", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

