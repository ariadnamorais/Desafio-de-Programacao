    private void jbCalcularInvestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCalcularInvestActionPerformed
        // TODO add your handling code here:
        
        int linha = jtAnunc.getSelectedRow();
        if (linha < 0){
            JOptionPane.showMessageDialog(this, "Selecione um anúncio!");
        }else{
            int pCondigoAnu = (int) jtAnunc.getValueAt(linha, 0);
            
            if (controllerAnuncio.calcularAnuncioController(pCondigoAnu)){
                carregarTabela();
                limparCampos();
                somarValorTotalInvestimento();
                JOptionPane.showMessageDialog(this, "Máximas calculadas com sucesso!");
            }else{
                JOptionPane.showMessageDialog(this,"Erro ao calcular máximas!");
            }
        }
    }//GEN-LAST:event_jbCalcularInvestActionPerformed

