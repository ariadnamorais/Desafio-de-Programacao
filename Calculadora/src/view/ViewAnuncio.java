    private void jbExcluAnuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluAnuncActionPerformed
        // TODO add your handling code here:
        int linha = jtAnunc.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um anúncio!");
        } else {
            int codigo = (int) jtAnunc.getValueAt(linha, 0);

            if (controllerAnuncio.excluirAnuncioController(codigo)) {
                carregarTabela();
                limparCampos();
                somarValorTotalInvestimento();
                JOptionPane.showMessageDialog(this, "Anúncio excluído com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir anúncio");
            }

        }
    }//GEN-LAST:event_jbExcluAnuncActionPerformed

