    private void jbSalvAnunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvAnunActionPerformed
        // TODO add your handling code here:
        modelAnuncio = new ModelAnuncio();
        modelAnuncio.setAnuDescricao(jtfNomeDescricao.getText());
        modelAnuncio.setAnuCliente(jtfCliente.getText());
        modelAnuncio.setAnuValinvest(new Formatador().converterVirgulaParaPonto(jtfInvPDia.getText()));
        modelAnuncio.setAnuDateinicio(Double.parseDouble(jffDateInic.getText()));
        modelAnuncio.setAnuDatefim(Double.parseDouble(jffDateTerm.getText()));
        controllerAnuncio.salvarAnuncioController(modelAnuncio);
        carregarTabela();
        limparCampos();
        somarValorTotalInvestimento();
    }//GEN-LAST:event_jbSalvAnunActionPerformed

